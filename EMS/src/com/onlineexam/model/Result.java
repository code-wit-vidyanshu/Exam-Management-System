package com.onlineexam.model;

public class Result {
    private int resultId; // Generated by default as identity
    private int organisationId; // Not null
    private int subjectId; // Not null
    private int studentId;
    private int currentAttempt;
    private int totalMarks; // Not null
    private String status; // Varchar2(10) NOT NULL
    private String description; // Varchar

    // Constructors, getters, setters, and other methods omitted for brevity
    
    // Constructor
    public Result( int organisationId, int subjectId, int studentId, int currentAttempt,
                  int totalMarks, String status, String description) {
        this.organisationId = organisationId;
        this.subjectId = subjectId;
        this.studentId = studentId;
        this.currentAttempt = currentAttempt;
        this.totalMarks = totalMarks;
        this.status = status;
        this.description = description;
    }

    // Getters and Setters
    public int getResultId() {
        return resultId;
    }

    public void setResultId(int resultId) {
        this.resultId = resultId;
    }

    public int getOrganisationId() {
        return organisationId;
    }

    public void setOrganisationId(int organisationId) {
        this.organisationId = organisationId;
    }

    public int getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(int subjectId) {
        this.subjectId = subjectId;
    }

    public int getStudentId() {
        return studentId;
    }

    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    public int getCurrentAttempt() {
        return currentAttempt;
    }

    public void setCurrentAttempt(int currentAttempt) {
        this.currentAttempt = currentAttempt;
    }

    public int getTotalMarks() {
        return totalMarks;
    }

    public void setTotalMarks(int totalMarks) {
        this.totalMarks = totalMarks;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "Result{" +
                "resultId=" + resultId +
                ", organisationId=" + organisationId +
                ", subjectId=" + subjectId +
                ", studentId=" + studentId +
                ", currentAttempt=" + currentAttempt +
                ", totalMarks=" + totalMarks +
                ", status='" + status + '\'' +
                ", description='" + description + '\'' +
                '}';
    }
}
